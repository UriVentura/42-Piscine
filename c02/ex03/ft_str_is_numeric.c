#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	a;

	a = 1;
	i = 0;
	while (str[i] != '\0' && a == 1)
	{
		if ((str[i] < '0') || (str[i] > '9'))
		{
			a = 0;
		}
		i++;
	}
	return (a);
}
