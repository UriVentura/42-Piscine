char	*ft_strlowcase(char *str)
{
	char	*str1;

	str1 = str;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
			*str = (*str + 32);
		str++;
	}
	str = str1;
	return (str);
}
