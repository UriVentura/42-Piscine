int	ft_str_is_alpha(char *str)
{
	int	i;
	int	a;

	a = 1;
	i = 0;
	while (str[i] != '\0' && a == 1)
	{
		if ((str[i] < 'A' || (str[i] > 'Z'))
			&& ((str[i] < 'a') || str[i] > 'z'))
		{
			a = 0;
		}
		i++;
	}
	return (a);
}
